//Операторы присвоивания
let age = 23;
age += 2; // age = age + 2
age -= 3; // age = age - 3
age *= 2; // age = age * 2
age /= 2; // age = age / 2
age++; // age + 1
age-- // age - 1

console.log(age)

// операторы сравнение
const vasia = 20
console.log(age > vasia)
console.log(age >= vasia)
console.log(age < vasia)
console.log(age <= vasia)
console.log(age == vasia)